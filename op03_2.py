#Простой калькулятор: Напишите программу-калькулятор, которая выполняет одну из
#операций (+, -, *, /) над двумя введенными пользователем числами.


# Функция для выполнения операций
def calculator():
    print("Простой калькулятор")
    print("Введите два числа и выберите операцию:")

    # Получаем два числа от пользователя
    num1 = float(input("Введите первое число: "))
    num2 = float(input("Введите второе число: "))

    # Выводим доступные операции
    print("Выберите операцию:")
    print("1. Сложение (+)")
    print("2. Вычитание (-)")
    print("3. Умножение (*)")
    print("4. Деление (/)")

    # Получаем выбор операции от пользователя
    operation = input("Введите номер операции (1/2/3/4): ")

    # Выполняем соответствующую операцию
    if operation == '1':
        result = num1 + num2
        print(f"{num1} + {num2} = {result}")
    elif operation == '2':
        result = num1 - num2
        print(f"{num1} - {num2} = {result}")
    elif operation == '3':
        result = num1 * num2
        print(f"{num1} * {num2} = {result}")
    elif operation == '4':
        if num2 != 0:
            result = num1 / num2
            print(f"{num1} / {num2} = {result}")
        else:
            print("Ошибка: Деление на ноль!")
    else:
        print("Неверный ввод. Пожалуйста, выберите правильную операцию.")


# Запуск калькулятора
calculator()
