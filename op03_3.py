#Определение количества дней в месяце: Пользователь вводит номер месяца (1-12).
#Выведите количество дней в этом месяце, не учитывая високосный год.


def days_in_month(month):
    # Словарь с количеством дней для каждого месяца
    days_in_months = {
        1: 31,  # Январь
        2: 28,  # Февраль
        3: 31,  # Март
        4: 30,  # Апрель
        5: 31,  # Май
        6: 30,  # Июнь
        7: 31,  # Июль
        8: 31,  # Август
        9: 30,  # Сентябрь
        10: 31,  # Октябрь
        11: 30,  # Ноябрь
        12: 31  # Декабрь
    }

    # Возвращаем количество дней в соответствующем месяце
    return days_in_months.get(
        month, "Неверный номер месяца. Пожалуйста, введите число от 1 до 12.")


# Запрашиваем у пользователя ввод месяца
month = int(input("Введите номер месяца (1-12): "))

# Получаем количество дней и выводим результат
days = days_in_month(month)
print(f"Количество дней в месяце {month}: {days}")
