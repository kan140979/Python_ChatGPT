#Написать программу, которая будет запрашивать у пользователя его имя и возраст, а
#затем выводить приветствие “Привет [имя]! Тебе [возраст]”.
#Можно усовершенствовать программу, чтобы она посчитала также количество прожитых
#месяцев, дней, часов.Просить помощи и разъяснений у ChatGPT, чтобы усовершенствовать
#программу — можно и нужно.

from datetime import datetime

# Запрашиваем у пользователя его имя
name = input("Введите ваше имя: ")

# Запрашиваем у пользователя его год рождения
birth_year = int(input("Введите ваш год рождения: "))
birth_month = int(input("Введите ваш месяц рождения (1-12): "))
birth_day = int(input("Введите ваш день рождения (1-31): "))

# Получаем текущее время
current_time = datetime.now()

# Создаем объект даты рождения
birth_date = datetime(birth_year, birth_month, birth_day)

# Вычисляем разницу между текущей датой и датой рождения
age_timedelta = current_time - birth_date

# Получаем количество прожитых дней
total_days = age_timedelta.days

# Вычисляем количество месяцев (приблизительно)
total_months = total_days // 30

# Вычисляем количество часов
total_hours = total_days * 24

# Выводим приветствие с расчетами
print(
    f"Привет {name}! Тебе {total_months} месяцев, {total_days} дней и {total_hours} часов."
)
